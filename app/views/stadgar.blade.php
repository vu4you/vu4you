<h3>§ 9 Styrelsens uppgift</h3>
<p>Föreningsstyrelsen består av en ordförande, en vice ordförande och minst 3 stycken ledamöter.</p>
<p>Styrelsen ska välja en person inom styrelsen till rollen som kassör.</p>
<p>Årsmötet kan välja minst 2 stycken suppleanter ifall om någon i styrelsen ej kan närvara vid till exempel ett styrelsemöte.</p>
<p>Ordförande har som uppgift att varje år kalla till och se till så att minst 4 stycken styrelsemöten genomförs.</p>
<p>Om styrelsen vill så har den rätt att bilda ett verkställande utskott på minst 2 personer.</p>

<p>Styrelsen har till uppgift att:
<ol>
<li>Planera och genomföra verksamhet, i enlighet med Vi Ungas grundidéer och stadgar</li>
<li>Leda föreningen, samt göra verksamhetsberättelse och bokslut</li>
<li>Betala avgifter och lämna in de rapporter, ge den information och insyn i verksamhet och förvaltning som distrikt och riksorganisation önskar samt medlemmar önskar.</li>
<li>Föra medlemmarnas talan enligt Förbundet Vi Ungas grundidéer</li>
<li>Stå upp för Vi Ungas värderingar och se till att de efterföljs</li>
</ol>
</p>
<h3>§ 10 Vilande förening</h3>
<p>En förening som inte bedriver verksamhet eller inte kan fullfölja sina åtaganden gentemot
medlemmarna kan förklaras vilande genom beslut på föreningårsmötet. Om årsrapport och
valrapport inte inlämnats senast den 15 mars kan distrikt eller, i de fall sådant inte finns, förbundet 
fatta beslut om att lägga föreningen vilande. Under tiden föreningen är vilande skall föreningens tillgångar 
förvaltas av särskilt utsedd teckningsrättsinnehavare. Om ingen lämplig person finns skall föreningens 
konton spärras och handkassan lämnas till distrikt eller, i de fall sådant inte finns, förbundet. Beslut 
om att utse teckningsrättsinnehavare eller beslut om att spärra konton fattas av i första hand föreningen 
annars av distrikt eller, i de fall sådant inte finns, förbundet.</p>

<h3>§ 11 Upplösning</h3>
<p>En förening som inte kan fullfölja sina åtaganden gentemot medlemmarna skall upplösas.
Föreningen kan inte upplösas så länge tre medlemmar i föreningen vill fortsätta arbetet. Föreningen
upplöses genom beslut på föreningsårsmöte. Om det inte är möjligt att genomföra
föreningårsmöte, kan föreningen upplösas genom beslut av distrikt eller, i de fall sådant inte
finns, förbundet. Vid upplösning tillfaller föreningens tillgångar, efter reglering av eventuella
skulder, distrikt eller, i de fall sådant inte finns, förbundet.</p>

