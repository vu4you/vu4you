<h1>Stadgar, Vi Unga 4you</h1>

<h3>§ 1 Föreningens namn</h3>
<p>Föreningens namn är ”Vi Unga 4you”. Föreningen är en lokalavdelning i Förbundet Vi Unga.</p>

<h3>§ 2 Styrelsens säte</h3>
<p>Styrelsen har sitt säte i Almunge, Uppsala kommun.</p>

<h3>§ 3 Föreningsform</h3>
<p>Föreningen är en ideell förening som är en lokalavdelning i Förbundet Vi Unga. Föreningen är
ett organisationsled i Förbundet Vi Unga. Föreningen följer de egna och alla ovanstående
organisationsleds stadgar inom Förbundet Vi Unga.</p>

<h3>§ 4 Stadgeändring</h3>
<p>För ändring av föreningens stadgar, fordras en majoritet av de rösterna vid ordinarie årsmöte alternativt 
vid ett enhälligt beslut på extrainkallat årsmöte. Förslag till ändring av föreningens stadgar skall utsändas 
minst 7 dagar före årsmötet.</p>

<h3>§ 5 Giltighet</h3>
<p>Då tillägg till grundstadgan eller ändring görs, träder stadgarna i kraft först då
Förbundsstyrelsen godkänt dem.<p>

<h3>§ 6 Medlemskap</h3>
<p>Alla fysiska personer som accepterar Vi Unga-föreningens stadgar kan bli medlemmar i
Förbundet Vi Unga. En fysisk person är medlem i Förbundet Vi Unga då den erlagt
medlemsavgift och är upptagen i medlemsregistret.</p>

<h3>§ 7 Motionsrätt</h3>
<p>Alla medlemmar i föreningen har rätt att lämna motion till föreningens årsmöte. Motionen skall
Vara underskriven av minst en motionär. Motionen skall vara Styrelsen tillhanda
senast 7 dagar före årsmötet.</p>

<h3>§ 8 Föreningårsmötet</h3>
<p>Årsmötet är föreningens högsta beslutande församling. Ordinarie årsmöte skall hållas varje år
senast den 28 februari. Extra årsmöte hålls då Styrelsen finner anledning till det eller
då minst 1/3 av medlemmarna skriftligen begär det. Vid extra årsmöte behandlas bara den
fråga som är orsak till det extra årsmötet, samt val av personer som för mötet skull som är nödvändigt 
såsom mötesordförande, justeringsmän. Styrelsen skall kalla till det ordinarie årsmötet senast 14
dagar före mötet.</p>

<p>Rösträtt vid föreningens årsmöte har alla närvarande som är med i föreningen samma år som årsmötet 
hålls. Det tas ej hänsyn till att du har varit medlem följande år. Den avgående styrelsen har inte 
rösträtt i fråga om Styrelsens ansvarsfrihet. Den nyvalda Styrelsen har inte rösträtt i frågan
om val av revisorer och revisorsuppleant. Personal i Förbundet Vi Unga har inte rösträtt.</p>

<p>Vid ordinarie årsmöte ska följande dagordning följas om inte ändring har informerats medlemmarna 
